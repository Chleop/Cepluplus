#include "MutantStack.hpp"

// **************************************************************************//
//                         CONSTRUCTOR / DESTRUCTOR                          //
// **************************************************************************//

template < typename T >
MutantStack<T>::MutantStack(void)
{
    return;
}

template < typename T >
MutantStack<T>::MutantStack(const MutantStack & src)
{
    *this = src;
}

template < typename T >
MutantStack<T>::~MutantStack(void)
{
    return;
}


// **************************************************************************//
//                                 OPERATORS                                 //
// **************************************************************************//

template < typename T >
MutantStack<T> & MutantStack<T>::operator = (const MutantStack<T> & rhs)
{
    if (rhs != *this)
        return *this;
}


